    private void btnaccMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnaccMouseClicked
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih transaksi terlebih dahulu!");
        return;
    }

    // Mendapatkan ID transaksi dari kolom yang sesuai di jTable1
    int idTransaksi = (int) jTable1.getValueAt(selectedRow, 2); // Asumsikan kolom ID ada di kolom pertama

    // Memperbarui status transaksi di database
    Connection con = database.getConnection();
    String query = "UPDATE transaksi SET status = ? WHERE id_transaksi = ?";
    try (PreparedStatement pst = con.prepareStatement(query)) {
        pst.setString(1, "Berhasil");
        pst.setInt(2, idTransaksi);
        int affectedRows = pst.executeUpdate();
        
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Status transaksi berhasil diperbarui!");
            loadData(); // Memperbarui tampilan tabel setelah perubahan
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui status transaksi!");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memperbarui status transaksi!");
    }
    }//GEN-LAST:event_btnaccMouseClicked

