 private void loadData() {
    Connection con = database.getConnection();
    String query = "SELECT id_user, id_mata_uang, id_transaksi, tanggal_transaksi FROM transaksi WHERE status = 'Pending'";
    try (PreparedStatement pst = con.prepareStatement(query)) {
//        pst.setInt(1, idGame);
        ResultSet rs = pst.executeQuery();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        while (rs.next()) {
            Object[] row = {
                rs.getInt("id_user"),
                rs.getInt("id_mata_uang"),
                rs.getInt("id_transaksi"),
                rs.getString("tanggal_transaksi"),
            };
            model.addRow(row);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memuat data!");
    }
}
