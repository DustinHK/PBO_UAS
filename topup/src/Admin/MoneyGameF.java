    private void btnhapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnhapusMouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
            return;
        }

        int id_mata_uang = (int) jTable1.getValueAt(row, 3); // Asumsi ID ada di kolom ke-6

        Connection con = database.getConnection();
        String query = "DELETE FROM mata_uang_game WHERE id_mata_uang = ?";
        try (PreparedStatement pst = con.prepareStatement(query)) {
            pst.setInt(1, id_mata_uang);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            loadData(idGame);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data!");
        }
    }//GEN-LAST:event_btnhapusMouseClicked

