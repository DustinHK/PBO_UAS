private void tambahubah() {
    String nama_mata_uang = txtuang.getText();
    String jumlah = txtjumlah.getText();
    String harga = txtharga.getText();
    String id_mata_uang = txtIdMataUangGame.getText(); // Ini adalah field tersembunyi atau tidak terlihat oleh user

    Connection con = database.getConnection();
    
      try {
        if (id_mata_uang.isEmpty()) {
            String query = "INSERT INTO mata_uang_game (nama_mata_uang, jumlah, harga, id_game) VALUES (?, ?, ?, ?)";
            try (PreparedStatement pst = con.prepareStatement(query)) {
                pst.setString(1, nama_mata_uang);
                pst.setInt(2, Integer.parseInt(jumlah));
                pst.setBigDecimal(3, new BigDecimal(harga));
                pst.setInt(4, idGame); // Asumsi idGame adalah variabel global atau diperoleh dari tempat lain
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            }
        } else {
            String query = "UPDATE mata_uang_game SET nama_mata_uang = ?, jumlah = ?, harga = ? WHERE id_mata_uang = ?";
            try (PreparedStatement pst = con.prepareStatement(query)) {
                pst.setString(1, nama_mata_uang);
                pst.setInt(2, Integer.parseInt(jumlah));
                pst.setBigDecimal(3, new BigDecimal(harga));
                pst.setInt(4, Integer.parseInt(id_mata_uang));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            }
        }
        loadData(idGame); // Memuat ulang data dengan idGame yang relevan
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data!");
    }
}
    private void loadData(int idGame) {
    Connection con = database.getConnection();
    String query = "SELECT * FROM mata_uang_game WHERE id_game = ?";
    try (PreparedStatement pst = con.prepareStatement(query)) {
        pst.setInt(1, idGame);
        ResultSet rs = pst.executeQuery();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        while (rs.next()) {
            Object[] row = {
                rs.getString("nama_mata_uang"),
                rs.getInt("jumlah"),
                rs.getBigDecimal("harga"),
                rs.getInt("id_mata_uang"),
                rs.getInt("id_game")
            };
            model.addRow(row);
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat memuat data!");
    }
}

